using System.Runtime.InteropServices;

namespace Fwht.Bench;

public static unsafe class Masks
{
    public const int STAGES = 9;
    public const int POOL_BYTES = STAGES * 64;

    private static readonly ulong[] Lanes =
    {
        0xAAAAAAAAAAAAAAAAUL, 0xAAAAAAAAAAAAAAAAUL, 0xAAAAAAAAAAAAAAAAUL, 0xAAAAAAAAAAAAAAAAUL, 0xAAAAAAAAAAAAAAAAUL, 0xAAAAAAAAAAAAAAAAUL, 0xAAAAAAAAAAAAAAAAUL, 0xAAAAAAAAAAAAAAAAUL,
        0xCCCCCCCCCCCCCCCCUL, 0xCCCCCCCCCCCCCCCCUL, 0xCCCCCCCCCCCCCCCCUL, 0xCCCCCCCCCCCCCCCCUL, 0xCCCCCCCCCCCCCCCCUL, 0xCCCCCCCCCCCCCCCCUL, 0xCCCCCCCCCCCCCCCCUL, 0xCCCCCCCCCCCCCCCCUL,
        0xF0F0F0F0F0F0F0F0UL, 0xF0F0F0F0F0F0F0F0UL, 0xF0F0F0F0F0F0F0F0UL, 0xF0F0F0F0F0F0F0F0UL, 0xF0F0F0F0F0F0F0F0UL, 0xF0F0F0F0F0F0F0F0UL, 0xF0F0F0F0F0F0F0F0UL, 0xF0F0F0F0F0F0F0F0UL,
        0xFF00FF00FF00FF00UL, 0xFF00FF00FF00FF00UL, 0xFF00FF00FF00FF00UL, 0xFF00FF00FF00FF00UL, 0xFF00FF00FF00FF00UL, 0xFF00FF00FF00FF00UL, 0xFF00FF00FF00FF00UL, 0xFF00FF00FF00FF00UL,
        0xFFFF0000FFFF0000UL, 0xFFFF0000FFFF0000UL, 0xFFFF0000FFFF0000UL, 0xFFFF0000FFFF0000UL, 0xFFFF0000FFFF0000UL, 0xFFFF0000FFFF0000UL, 0xFFFF0000FFFF0000UL, 0xFFFF0000FFFF0000UL,
        0xFFFFFFFF00000000UL, 0xFFFFFFFF00000000UL, 0xFFFFFFFF00000000UL, 0xFFFFFFFF00000000UL, 0xFFFFFFFF00000000UL, 0xFFFFFFFF00000000UL, 0xFFFFFFFF00000000UL, 0xFFFFFFFF00000000UL,
        0UL, ~0UL, 0UL, ~0UL, 0UL, ~0UL, 0UL, ~0UL,
        0UL, 0UL, ~0UL, ~0UL, 0UL, 0UL, ~0UL, ~0UL,
        0UL, 0UL, 0UL, 0UL, ~0UL, ~0UL, ~0UL, ~0UL
    };

    public static byte* Build()
    {
        byte* pool = (byte*)NativeMemory.AlignedAlloc(POOL_BYTES, 64);
        ulong* qwords = (ulong*)pool;
        for (int i = 0; i < Lanes.Length; i++)
        {
            qwords[i] = Lanes[i];
        }
        return pool;
    }

    public static void Free(byte* pool)
    {
        NativeMemory.AlignedFree(pool);
    }
}
